    	<h1><i>Burwell v. Hobby Lobby Stores, Inc.</i></h1>
    	<section class="text dropshadow">
            <p>Hobby Lobby is an arts and crafts supply company founded and owned by David Green
            that is privately held and employs over 23,000 people. Green explicitly operates his business
            according to his Christian faith, closing down on Sundays, even if the result is a loss of profit, so
            his employees can take the day off for worship, family, and leisure. Under the Affordable Care
            Act, health care provided to employees by employers must cover the cost of FDA-approved
            contraceptive medications. Guided by Christian principles, Green refused to do as such. Hobby
            Lobby filed a lawsuit against the Secretary of Health and Human Services, Sylvia Burwell
            (originally against Kathleen Sebelius prior to her resignation) in protest of this new government
            mandate.</p>

            <p>The legislation of access to contraceptives in this case brings to mind the cases <i>Griswold
            v. Connecticut</i> and <i>Eisenstadt v. Baird</i>. Mainly, this case revolves around a conflict of rights.
            Green believes he has a right to religious freedom, guaranteed by the First Amendment, that
            allows him to refrain from engaging in activities that violate his religious views. At the same
            time, there is a clear right to access to contraceptives established in <i>Griswold</i> and <i>Eisenstadt</i>.
            One could make an argument, weak as it may be, that despite not having contraceptive medicine
            covered by employer health care, those employees who desire it do have the ability and right to
            purchase it independent of health care coverage; however, it is clear that Green&rsquo;s refusal of
            coverage is a hinderance in the obtainment of contraceptive medications that places an undue
            burden of cost upon those who work for him that others do not have to contend with. The
            question is: does Green&rsquo;s religious right to freedom give him power to extend his moral views on
            contraceptives over his employees? By refusing to pay for contraceptives for his employees,
            Green is essentially pushing his religious and moral views onto his employees. Additionally, by
            doing so, he is saying that his right to defend his morals is greater than the right to bodily
            autonomy of thousands of employees.</p>

            <p>There is a well-founded, important tradition of religious freedom in United States law and
            culture. This right is founded in the idea of autonomy and self-governance. As Mill says, the
            only person who knows what moral (or religious) values suit one the best is that person alone.
            This right of control, however, begins and stops with the individual. The Constitution affords no
            such right to control the religious or moral views of others to an individual, no matter the
            relationship between the two people. As seen in <i>West Virginia State Board of Education v.
            Barnette</i> (the case that overturned <i>Minersville School District v. Gobitis</i>), the Supreme Court has
            held that religious freedom allows individuals to express and practice their beliefs freely and
            without impediment. This decision was incredibly important in supporting religious freedom in
            the United States; when <i>Minersville</i> was originally decided, over 1,000 Jehovah's Witnesses
            were physically attacked across the country and labeled traitors as a result of the court&rsquo;s decision
            before it was overturned (Peters). Despite the unarguable importance of religious freedom, it
            would be ludicrous to say that one student&rsquo;s right not to salute the flag based on a personal
            interpretation of Exodus grants them the ability, in order to suit their individual beliefs, to force
            all other students in the room not to salute the flag. Even if it were the teacher, a person in a
            position of power, whose religious beliefs precluded saluting the flag, they would still have no
            right to force their views upon their subordinate students just as an employer cannot force their
            beliefs on employees.</p>

            <p>Similar to this was the infamous Kentucky county clerk, Kim Davis. Following the
            holding of <i>Obergefell v. Hodges</i> which allowed same-sex couples to marry nation-wide, she
            refused to issue marriage licenses to these couples in her country on religious grounds. She was
            ordered by a court to issue the licenses. Following her refusal, she was found in contempt of
            court and jailed. Obviously the court did not view her right to religious freedom as proper
            justification for the denial of a fundamental right to all of the same sex couples in her county.
            I feel to properly examine the concept of religious freedom as it appears in this case, it is
            necessary to divorce this situation from Christianity, to which our society has a clear bias.
            Suppose Green were a devout Muslim. Among the beliefs outlined in the Qur&rsquo;an is the sinfulness
            of intoxication. If Green were to demand that all of his employees refrain from drinking alcohol,
            there would be massive public outrage. Perhaps he could even go as far as to require all female
            employees to wear burkas or follow the Second Pillar of Islam and pray to Allah five times a
            day, but of course that would be <i>absurd</i> and no doubt result in public indignation. But in the
            current situation with Christian morals, there is an outpouring of support. Following the logic
            used, could Green not also prohibit his employees from other things the Bible forbids: wearing
            clothes of mixed fiber, eating meat of animals with uncloven hooves, or perhaps even saluting
            the flag? The public's lack of outrage is a clear indication of our acceptance and integration of
            Christian values into society. The Establishment Clause of the constitution, however, had
            generally been interpreted to forbid the government from establishing a national religion and
            more importantly from favoring or benefiting one religion over another and our society's ideal of
            equality tell us the same. Finding in favor of Hobby Lobby in this case&mdash;when, substituted with
            any other religion, there would have been a much different outcome&mdash;violates this tenet of our
            society and our constitution.</p>

            <p>Aside from the deontological argument, from a utilitarian standpoint the outcome is the
            same. If either Green or his employees see their rights as being infringed upon if contraceptive
            medicines are covered or if they are not, then utilitarianism guides us toward the solution that
            leads to the greatest happiness and the violation of the fewest rights. Given the choice between
            offending one man&rsquo;s morals and depriving potentially 23,000 employees of medication which
            can prevent unintended pregnancy and a myriad of other effects unrelated to pregnancy, the
            answer is clear. Granting the employees access to contraceptive medications covered by health
            care is the best option.</p>

            <p>The decision of this case is both logically and morally troubling and leads to many
            questions regarding the response to this conflict of rights and the lauded and favored position of
            Christianity in United State&rsquo;s law and society.</p>
